import mongoose from "mongoose";

const chatSchema = mongoose.Schema({
  fullName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  mobileNumber: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  
},{timestamps:true,});
const Chat = mongoose.model("Chat", chatSchema);
export default Chat;
